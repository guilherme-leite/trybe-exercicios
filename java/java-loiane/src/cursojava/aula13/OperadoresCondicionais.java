package cursojava.aula13;

public class OperadoresCondicionais {

	public static void main(String[] args) {
		int valor01 = 1;
		int valor02 = 2;
		
		System.out.println("valor01 == valor02 ? " + (valor01 == valor02));
		System.out.println("valor01 != valor02 ? " + (valor01 != valor02));
		System.out.println("valor01 > valor02 ? " + (valor01 > valor02));
		System.out.println("valor01 < valor02 ? " + (valor01 < valor02));
		System.out.println("valor01 >= valor02 ? " + (valor01 >= valor02));
		System.out.println("valor01 <= valor02 ? " + (valor01 <= valor02));
	}

}
